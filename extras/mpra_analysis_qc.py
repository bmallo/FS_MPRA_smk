def chromatin_shift_test(rd, wt_idx, var_idx, min_nuc=None, max_nuc=None):
    """Test whether the variant shifts the NC distribution vs WT."""
    logging.info("=" * 60)
    logging.info("PHASE 2: Chromatin Shift Test")
    logging.info("=" * 60)

    wt_f = rd.get_indices_filtered(wt_idx, min_nuc=min_nuc, max_nuc=max_nuc)
    var_f = rd.get_indices_filtered(var_idx, min_nuc=min_nuc, max_nuc=max_nuc)
    wt_nc = rd.nuc_counts[wt_f]
    var_nc = rd.nuc_counts[var_f]
    wt_nc_valid = wt_nc[wt_nc >= 0]
    var_nc_valid = var_nc[var_nc >= 0]

    result = {'wt_n': len(wt_f), 'var_n': len(var_f),
              'wt_n_valid_nc': len(wt_nc_valid),
              'var_n_valid_nc': len(var_nc_valid)}

    if len(wt_nc_valid) == 0 or len(var_nc_valid) == 0:
        result['error'] = 'insufficient_nc_data'
        return result

    ks_stat, ks_pval = scipy_stats.ks_2samp(wt_nc_valid, var_nc_valid)
    delta_mean = float(np.mean(var_nc_valid) - np.mean(wt_nc_valid))
    delta_median = float(np.median(var_nc_valid) - np.median(wt_nc_valid))

    # Chi-squared on binned NC
    all_nc = np.concatenate([wt_nc_valid, var_nc_valid])
    unique_nc = np.unique(all_nc)
    wt_hist = np.array([np.sum(wt_nc_valid == nc) for nc in unique_nc])
    var_hist = np.array([np.sum(var_nc_valid == nc) for nc in unique_nc])
    total_wt, total_var = wt_hist.sum(), var_hist.sum()
    chi2, chi2_p = np.nan, np.nan
    if total_wt > 0 and total_var > 0:
        expected_var = wt_hist / total_wt * total_var
        valid_bins = expected_var >= 5
        if np.sum(valid_bins) >= 2:
            obs = var_hist[valid_bins]
            exp = expected_var[valid_bins]
            exp = exp * (obs.sum() / exp.sum())
            chi2, chi2_p = scipy_stats.chisquare(obs, exp)

    wt_u, wt_c = np.unique(wt_nc_valid, return_counts=True)
    var_u, var_c = np.unique(var_nc_valid, return_counts=True)

    result.update({
        'ks_stat': float(ks_stat), 'ks_pval': float(ks_pval),
        'delta_mean_nc': delta_mean, 'delta_median_nc': delta_median,
        'wt_mean_nc': float(np.mean(wt_nc_valid)),
        'var_mean_nc': float(np.mean(var_nc_valid)),
        'wt_median_nc': float(np.median(wt_nc_valid)),
        'var_median_nc': float(np.median(var_nc_valid)),
        'chi2_stat': float(chi2) if not np.isnan(chi2) else None,
        'chi2_pval': float(chi2_p) if not np.isnan(chi2_p) else None,
        'wt_nc_distribution': {int(k): int(v) for k, v in zip(wt_u, wt_c)},
        'var_nc_distribution': {int(k): int(v) for k, v in zip(var_u, var_c)},
    })

    logging.info(f"  WT: n={len(wt_f):,}, mean_nc={result['wt_mean_nc']:.1f}")
    logging.info(f"  Var: n={len(var_f):,}, mean_nc={result['var_mean_nc']:.1f}")
    logging.info(f"  KS stat={ks_stat:.4f}, p={ks_pval:.4e}")
    logging.info(f"  delta mean NC = {delta_mean:+.2f}")
    return result


