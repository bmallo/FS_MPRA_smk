#!/usr/bin/env python3
"""
mpra_empirical_analysis.py — Empirical MPRA Fiber-seq Footprint Analysis (v0.1.0)

An empirical-first approach to detecting variant effects on protein-DNA
interactions from Fiber-seq MPRA data.

CORE APPROACH:
  The primary test statistic is the raw occupancy difference:
    Δ_obs(pos) = variant_occ(pos) - WT_occ(pos)

  Significance is assessed by comparing Δ_obs to an empirical null
  distribution generated by subsampling WT reads at variant-like coverage
  (NC-matched to the ground truth WT NC distribution).

  Cluster detection uses adaptive thresholding: positions where |Δ_obs|
  exceeds the per-position (1-α) quantile of |Δ_null| are merged into
  clusters. Cluster significance is assessed by comparing the cluster's
  sum(|Δ|) to the null cluster-sum distribution.

ANALYSIS PHASES:
  1. Parse & Setup:     BAM parsing, NC distributions, filter grid
  2. Chromatin Shift:   WT vs variant NC distribution comparison
  3. Null Calibration:  Empirical null at primary coverage + coverage grid
  4. Variant Testing:   Position-level and cluster-level significance
  5. Filter Optimization: Detection metrics across NC filter grid
  6. Power Analysis:    Detection rate vs coverage depth

OUTPUTS:
  - HDF5 file with full results, null distributions, and cluster calls
  - Multi-page diagnostic PDF
  - Jupyter notebook template for interactive exploration

Author: Ben / Stergachis Lab, University of Washington
"""

