def main():
    args = parse_args()
    setup_logging(args.verbose)

    if args.threads is None:
        args.threads = min(os.cpu_count() or 1, args.n_null_iterations)

    logging.info(f"mpra_empirical_analysis.py v{VERSION}")
    logging.info(f"BAM: {args.bam}")
    logging.info(f"Output: {args.output}")
    logging.info(f"Threads: {args.threads}")
    start_time = time.time()

    if args.pdf is None:
        args.pdf = args.output.rsplit('.', 1)[0] + '.pdf'
    if args.notebook is None:
        args.notebook = args.output.rsplit('.', 1)[0] + '_explorer.ipynb'

    size_bins = [FOOTPRINT_BINS['sub_TF'], FOOTPRINT_BINS['TF'],
                 FOOTPRINT_BINS['PIC'], FOOTPRINT_BINS['NUC']]
    bin_labels = [b[2] for b in size_bins]

    target_chrom, target_start, target_end = parse_target_region(
        args.target_region)

    coverage_grid = [int(d) for d in args.coverage_grid.split(',')]
    min_nuc_grid = [int(d) for d in args.min_nuc_grid.split(',')]
    max_nuc_pctiles = [int(d) for d in args.max_nuc_percentiles.split(',')]

    # ================================================================
    # Phase 1: Parse BAM
    # ================================================================
    logging.info("=" * 60)
    logging.info("PHASE 1: Parse & Setup")
    logging.info("=" * 60)

    rd, ref_length, ref_name, parse_stats = parse_bam(
        args.bam, size_bins, bin_labels, target_chrom=target_chrom)

    wt_idx = rd.wt_indices
    logging.info(f"WT: {len(wt_idx):,}")
    for vid, vidx in rd.variant_indices.items():
        logging.info(f"Variant '{vid}': {len(vidx):,}")

    # Promoter region
    if args.promoter_region:
        ps = args.promoter_region.split('-')
        prom_s, prom_e = int(ps[0]), int(ps[1])
    elif target_start is not None:
        prom_s, prom_e = target_start, target_end
    else:
        prom_s, prom_e = 0, ref_length
    logging.info(f"Promoter: {prom_s}-{prom_e} ({prom_e - prom_s} bp)")

    analysis_region = (prom_s, prom_e)

    # Target variant
    if args.variant_id and args.variant_id in rd.variant_indices:
        tgt_vid = args.variant_id
        tgt_vidx = rd.variant_indices[tgt_vid]
        logging.info(f"Using specified variant: {tgt_vid} "
                     f"({len(tgt_vidx):,} reads)")
    elif args.variant_id and args.variant_id not in rd.variant_indices:
        logging.error(f"Variant '{args.variant_id}' not found in BAM. "
                      f"Available: {list(rd.variant_indices.keys())}")
        tgt_vid, tgt_vidx = None, None
    elif len(rd.variant_indices) == 1:
        tgt_vid = list(rd.variant_indices.keys())[0]
        tgt_vidx = rd.variant_indices[tgt_vid]
        logging.info(f"Auto-detected variant: {tgt_vid} "
                     f"({len(tgt_vidx):,} reads)")
    elif len(rd.variant_indices) > 1:
        # Multiple variants: pick the one with the most reads
        tgt_vid = max(rd.variant_indices.keys(),
                      key=lambda v: len(rd.variant_indices[v]))
        tgt_vidx = rd.variant_indices[tgt_vid]
        logging.warning(f"Multiple variants found: "
                        f"{list(rd.variant_indices.keys())}")
        logging.warning(f"Auto-selected largest: {tgt_vid} "
                        f"({len(tgt_vidx):,} reads). "
                        f"Use --variant-id to override.")
    else:
        tgt_vid, tgt_vidx = None, None
        logging.error("No variant reads found in BAM!")

    vpos = args.variant_position

    # ================================================================
    # Ground Truth NC
    # ================================================================
    ground_truth_nc = compute_ground_truth_nc(
        rd, wt_idx, min_nuc=args.min_nuc, max_nuc=args.max_nuc)

    # ================================================================
    # Phase 2: Chromatin Shift
    # ================================================================
    chromatin_test = None
    if tgt_vidx is not None:
        chromatin_test = chromatin_shift_test(
            rd, wt_idx, tgt_vidx,
            min_nuc=args.min_nuc, max_nuc=args.max_nuc)
    else:
        logging.warning("Skipping Phase 2 (chromatin shift): "
                        "no variant identified.")

    # ================================================================
    # Phase 3: Null Calibration
    # ================================================================
    null_results_primary = None
    null_results_by_depth = {}

    if ground_truth_nc is not None and tgt_vidx is not None:
        var_f = rd.get_indices_filtered(tgt_vidx, min_nuc=args.min_nuc,
                                         max_nuc=args.max_nuc)
        primary_depth = len(var_f)
        logging.info(f"Variant reads after NC filter: {primary_depth:,} "
                     f"(from {len(tgt_vidx):,} raw)")

        if primary_depth < 20:
            logging.warning(f"Too few variant reads after filtering "
                            f"({primary_depth}). Skipping null calibration. "
                            f"Check --min-nuc / --max-nuc settings.")
        else:
            logging.info("=" * 60)
            logging.info("PHASE 3: Null Calibration")
            logging.info("=" * 60)
            logging.info(f"Primary null depth: {primary_depth}")

            t0 = time.time()
            null_results_primary = run_null_calibration(
                rd, wt_idx, ground_truth_nc, coverage_level=primary_depth,
                min_nuc=args.min_nuc, max_nuc=args.max_nuc,
                n_iterations=args.n_null_iterations,
                random_seed=args.random_seed,
                analysis_region=analysis_region,
                cluster_threshold_quantile=args.cluster_threshold_quantile,
                absolute_delta_threshold=args.absolute_delta_threshold,
                gap_tolerance=args.gap_tolerance,
                merge_distance=args.merge_distance,
                n_workers=args.threads)
            null_results_by_depth[primary_depth] = null_results_primary
            logging.info(f"  Primary null time: {time.time() - t0:.1f}s")

        # Coverage grid nulls (lightweight: fewer iterations)
        if not args.skip_power_analysis:
            grid_depths = [d for d in coverage_grid
                           if d != primary_depth and d <= len(var_f)]
            for depth in grid_depths:
                t1 = time.time()
                nr = run_null_calibration(
                    rd, wt_idx, ground_truth_nc, coverage_level=depth,
                    min_nuc=args.min_nuc, max_nuc=args.max_nuc,
                    n_iterations=min(200, args.n_null_iterations),
                    random_seed=args.random_seed + depth,
                    analysis_region=analysis_region,
                    cluster_threshold_quantile=args.cluster_threshold_quantile,
                    absolute_delta_threshold=args.absolute_delta_threshold,
                    gap_tolerance=args.gap_tolerance,
                    merge_distance=args.merge_distance,
                    n_workers=max(1, args.threads // 2))
                null_results_by_depth[depth] = nr
                logging.info(f"  Depth {depth} null: {time.time() - t1:.1f}s")

    # ================================================================
    # Phase 4: Variant Testing
    # ================================================================
    variant_results = None
    if (tgt_vidx is not None and null_results_primary is not None
            and ground_truth_nc is not None):
        variant_results = variant_effect_test(
            rd, wt_idx, tgt_vidx, ground_truth_nc, null_results_primary,
            prom_s, prom_e,
            min_nuc=args.min_nuc, max_nuc=args.max_nuc,
            random_seed=args.random_seed,
            cluster_threshold_quantile=args.cluster_threshold_quantile,
            absolute_delta_threshold=args.absolute_delta_threshold,
            gap_tolerance=args.gap_tolerance,
            merge_distance=args.merge_distance)

    # ================================================================
    # Phase 5: Filter Optimization
    # ================================================================
    filter_results = {}
    if (not args.skip_filter_optimization and tgt_vidx is not None
            and ground_truth_nc is not None):
        filter_results = filter_optimization(
            rd, wt_idx, tgt_vidx, bin_labels,
            min_nuc_grid, max_nuc_pctiles,
            prom_s, prom_e,
            variant_position=vpos,
            n_null_iterations=args.n_filter_null_iters,
            random_seed=args.random_seed,
            analysis_region=analysis_region,
            cluster_threshold_quantile=args.cluster_threshold_quantile,
            gap_tolerance=args.gap_tolerance,
            merge_distance=args.merge_distance,
            n_workers=args.threads)

    # ================================================================
    # Phase 6: Power Analysis
    # ================================================================
    power_results = {}
    if (not args.skip_power_analysis and tgt_vidx is not None
            and null_results_by_depth and ground_truth_nc is not None):
        power_results = power_analysis(
            rd, wt_idx, tgt_vidx, ground_truth_nc,
            null_results_by_depth, coverage_grid,
            prom_s, prom_e,
            min_nuc=args.min_nuc, max_nuc=args.max_nuc,
            n_resamples=args.n_power_resamples,
            random_seed=args.random_seed,
            variant_position=vpos,
            cluster_threshold_quantile=args.cluster_threshold_quantile,
            gap_tolerance=args.gap_tolerance,
            merge_distance=args.merge_distance)

    # ================================================================
    # Output
    # ================================================================
    write_results_hdf5(
        args.output, parse_stats, ground_truth_nc, chromatin_test,
        null_results_primary, null_results_by_depth, variant_results,
        filter_results, power_results,
        bin_labels, ref_length, ref_name, prom_s, prom_e, args)

    generate_diagnostic_pdf(
        args.pdf, chromatin_test, null_results_primary,
        variant_results, filter_results, power_results,
        bin_labels, ref_length, prom_s, prom_e,
        variant_position=vpos, analysis_region=analysis_region)

    generate_notebook_template(args.notebook, args.output, bin_labels)

    print_summary(chromatin_test, variant_results, power_results, bin_labels)

    elapsed = time.time() - start_time
