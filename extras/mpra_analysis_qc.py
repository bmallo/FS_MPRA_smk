def run_null_calibration(rd, wt_idx, ground_truth_nc, coverage_level,
                         min_nuc=None, max_nuc=None,
                         n_iterations=500, random_seed=42,
                         analysis_region=None,
                         cluster_threshold_quantile=0.95,
                         absolute_delta_threshold=None,
                         gap_tolerance=2, merge_distance=5,
                         n_workers=1):
    """Generate empirical null distribution at a given coverage level.

    Returns full null delta matrix, null cluster statistics, and WT occupancy.
    """
    logging.info(f"  Null calibration: N={coverage_level}, "
                 f"iters={n_iterations}, workers={n_workers}")

    bin_labels = rd.bin_labels
    n_bins = len(bin_labels)

    if analysis_region is not None:
        a_start, a_end = analysis_region
    else:
        a_start, a_end = 0, rd.ref_length
    analysis_length = a_end - a_start

    wt_f = rd.get_indices_filtered(wt_idx, min_nuc=min_nuc, max_nuc=max_nuc)
    n_wt = len(wt_f)

    col_indices = np.arange(a_start, a_end)
    wt_mats = {l: rd.coverage_matrices[l][np.ix_(wt_f, col_indices)]
               for l in bin_labels}

    # Full WT occupancy: stable baseline
    wt_occ = {l: wt_mats[l].mean(axis=0).astype(np.float64) for l in bin_labels}

    # NC pools (local indices into wt_mats rows)
    wt_nc = rd.nuc_counts[wt_f]
    nc_vals = ground_truth_nc['nc_vals']
    nc_fracs = ground_truth_nc['nc_fracs']

    wt_nc_pools = {}
    for nc_val in nc_vals:
        pool = np.where(wt_nc == nc_val)[0]
        if len(pool) > 0:
            wt_nc_pools[nc_val] = pool
    wt_nc_pools_serialized = [(nc_val, pool)
                               for nc_val, pool in wt_nc_pools.items()]

    nc_draw_counts = []
    for nc_val, frac in zip(nc_vals, nc_fracs):
        n_draw = max(1, int(round(frac * coverage_level)))
        if nc_val in wt_nc_pools:
            nc_draw_counts.append((nc_val, n_draw))

    master_rng = np.random.default_rng(random_seed)
    all_seeds = master_rng.integers(0, 2**63, size=n_iterations)

    # Serialize WT occ as list for pickling
    wt_occ_list = [wt_occ[l].tolist() for l in bin_labels]

    null_delta = np.zeros((n_iterations, n_bins, analysis_length),
                          dtype=np.float32)
    all_cluster_stats = []

    if n_workers > 1 and n_iterations > 1:
        shm_info, shm_objects = create_shared_matrices(wt_mats, bin_labels)

        chunk_size = max(1, n_iterations // n_workers)
        seed_chunks = []
        for i in range(0, n_iterations, chunk_size):
            seed_chunks.append(all_seeds[i:i + chunk_size])

        worker_args = [
            (chunk, shm_info, bin_labels, nc_draw_counts,
             wt_nc_pools_serialized, coverage_level, wt_occ_list,
             analysis_length, cluster_threshold_quantile, gap_tolerance,
             merge_distance, absolute_delta_threshold)
            for chunk in seed_chunks
        ]

        try:
            with ProcessPoolExecutor(max_workers=n_workers) as executor:
                futures = {executor.submit(_null_worker_shared, wa): ci
                           for ci, wa in enumerate(worker_args)}
                results_by_chunk = {}
                for future in as_completed(futures):
                    ci = futures[future]
                    results_by_chunk[ci] = future.result()
                # Reassemble in order
                offset = 0
                for ci in range(len(seed_chunks)):
                    delta_chunk, cluster_chunk = results_by_chunk[ci]
                    n_chunk = delta_chunk.shape[0]
                    null_delta[offset:offset + n_chunk] = delta_chunk
                    all_cluster_stats.extend(cluster_chunk)
                    offset += n_chunk
        finally:
            cleanup_shared_memory(shm_objects)
    else:
        for it in tqdm(range(n_iterations), desc="    Null iters",
                       disable=not HAS_TQDM):
            rng = np.random.default_rng(all_seeds[it])
            pv_indices = _nc_matched_batch_draw(
                nc_draw_counts, wt_nc_pools, 1, coverage_level, rng)[0]

            iter_clusters = {}
            for bi, label in enumerate(bin_labels):
                pv_occ = wt_mats[label][pv_indices].mean(axis=0)
                delta = pv_occ - wt_occ[label]
                null_delta[it, bi, :] = delta.astype(np.float32)

                abs_d = np.abs(delta)
                thresh = 0.0
                if cluster_threshold_quantile is not None:
                    thresh = np.percentile(abs_d,
                                           cluster_threshold_quantile * 100)
                if absolute_delta_threshold is not None:
                    thresh = max(thresh, absolute_delta_threshold)
                clusters = _detect_clusters_adaptive(
                    abs_d, thresh, gap_tolerance, merge_distance)
                iter_clusters[label] = {
                    'n_clusters': len(clusters),
                    'max_cluster_sum': (max(c['sum_abs_delta'] for c in clusters)
                                        if clusters else 0.0),
                    'cluster_sums': [c['sum_abs_delta'] for c in clusters],
                    'cluster_widths': [c['width'] for c in clusters],
                }
            all_cluster_stats.append(iter_clusters)

    # Summarize
    results = {
        'null_delta': null_delta, 'wt_occ': wt_occ,
        'coverage_level': coverage_level, 'n_iterations': n_iterations,
        'analysis_region': (a_start, a_end),
        'analysis_length': analysis_length,
        'summary': {}, 'null_cluster_sums': {},
        'null_cluster_widths': {}, 'null_max_cluster_sums': {},
    }

    for bi, label in enumerate(bin_labels):
        delta_all = null_delta[:, bi, :]
        pos_mean = delta_all.mean(axis=0)
        pos_std = delta_all.std(axis=0)
        abs_delta_flat = np.abs(delta_all).flatten()

        all_sums, all_widths, max_sums = [], [], []
        for cs in all_cluster_stats:
            if label in cs:
                all_sums.extend(cs[label]['cluster_sums'])
                all_widths.extend(cs[label]['cluster_widths'])
                max_sums.append(cs[label]['max_cluster_sum'])

        results['summary'][label] = {
            'pos_null_mean': pos_mean,
            'pos_null_std': pos_std,
            'global_abs_delta_percentiles': {
                p: float(np.percentile(abs_delta_flat, p))
                for p in [50, 75, 90, 95, 99]
            },
            'fpr_delta_gt_0.01': float(np.mean(abs_delta_flat > 0.01)),
            'fpr_delta_gt_0.02': float(np.mean(abs_delta_flat > 0.02)),
            'fpr_delta_gt_0.05': float(np.mean(abs_delta_flat > 0.05)),
        }
        results['null_cluster_sums'][label] = np.array(all_sums, dtype=np.float32)
        results['null_cluster_widths'][label] = np.array(all_widths, dtype=np.int32)
        results['null_max_cluster_sums'][label] = np.array(max_sums, dtype=np.float32)

        mean_clust = np.mean([cs[label]['n_clusters']
                              for cs in all_cluster_stats if label in cs])
        logging.info(f"    {label}: null |delta| p95="
                     f"{results['summary'][label]['global_abs_delta_percentiles'][95]:.4f}, "
                     f"clusters/iter={mean_clust:.1f}")

    return results


