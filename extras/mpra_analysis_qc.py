def _detect_clusters_adaptive(abs_delta, threshold, gap_tolerance=2,
                              merge_distance=5, min_width=3):
    """Detect clusters of positions with |delta| above threshold."""
    sig = abs_delta > threshold
    n = len(sig)
    if not np.any(sig):
        return []

    # Gap bridging
    if gap_tolerance > 0:
        bridged = sig.copy()
        for i in range(n):
            if not sig[i]:
                left = np.any(sig[max(0, i - gap_tolerance):i])
                right = np.any(sig[i + 1:min(n, i + gap_tolerance + 1)])
                if left and right:
                    bridged[i] = True
        sig = bridged

    labeled, n_clusters = ndimage.label(sig)
    raw_clusters = []
    for c in range(1, n_clusters + 1):
        pos = np.where(labeled == c)[0]
        raw_clusters.append({'start': int(pos[0]), 'end': int(pos[-1])})

    # Merge nearby
    if len(raw_clusters) > 1:
        merged = [raw_clusters[0].copy()]
        for c in raw_clusters[1:]:
            if c['start'] - merged[-1]['end'] <= merge_distance:
                merged[-1]['end'] = c['end']
            else:
                merged.append(c.copy())
        raw_clusters = merged

    clusters = []
    for c in raw_clusters:
        s, e = c['start'], c['end']
        width = e - s + 1
        if width < min_width:
            continue
        region_delta = abs_delta[s:e + 1]
        clusters.append({
            'start': s, 'end': e, 'width': width,
            'sum_abs_delta': float(np.sum(region_delta)),
            'max_abs_delta': float(np.max(region_delta)),
            'mean_abs_delta': float(np.mean(region_delta)),
            'peak_position': s + int(np.argmax(region_delta)),
        })
    return clusters


def _detect_clusters_from_mask(sig_mask, abs_delta, signed_delta,
                               gap_tolerance=2, merge_distance=5,
                               min_width=3):
    """Detect clusters from a boolean significance mask, with direction."""
    n = len(sig_mask)
    if not np.any(sig_mask):
        return []

    if gap_tolerance > 0:
        bridged = sig_mask.copy()
        for i in range(n):
            if not sig_mask[i]:
                left = np.any(sig_mask[max(0, i - gap_tolerance):i])
                right = np.any(sig_mask[i + 1:min(n, i + gap_tolerance + 1)])
                if left and right:
                    bridged[i] = True
        sig_mask = bridged

    labeled, n_clusters = ndimage.label(sig_mask)
    raw_clusters = []
    for c in range(1, n_clusters + 1):
        pos = np.where(labeled == c)[0]
        raw_clusters.append({'start': int(pos[0]), 'end': int(pos[-1])})

    if len(raw_clusters) > 1:
        merged = [raw_clusters[0].copy()]
        for c in raw_clusters[1:]:
            if c['start'] - merged[-1]['end'] <= merge_distance:
                merged[-1]['end'] = c['end']
            else:
                merged.append(c.copy())
        raw_clusters = merged

    clusters = []
    for c in raw_clusters:
        s, e = c['start'], c['end']
        width = e - s + 1
        if width < min_width:
            continue
        region_abs = abs_delta[s:e + 1]
        region_signed = signed_delta[s:e + 1]
        peak_idx = np.argmax(region_abs)
        clusters.append({
            'start': s, 'end': e, 'width': width,
            'sum_abs_delta': float(np.sum(region_abs)),
            'max_abs_delta': float(np.max(region_abs)),
            'mean_abs_delta': float(np.mean(region_abs)),
            'mean_signed_delta': float(np.mean(region_signed)),
            'peak_position': s + int(peak_idx),
            'direction': 'loss' if np.mean(region_signed) < 0 else 'gain',
        })
    return clusters


