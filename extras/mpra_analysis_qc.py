    logging.info(f"ANALYSIS SUMMARY (v{VERSION})")
    logging.info("=" * 70)

    if chromatin_test is not None:
        logging.info("")
        logging.info("--- Chromatin Shift ---")
        sig = "***" if chromatin_test.get('ks_pval', 1) < 0.001 else ""
        logging.info(f"  KS={chromatin_test.get('ks_stat', 0):.4f}, "
                     f"p={chromatin_test.get('ks_pval', 1):.2e} {sig}, "
                     f"delta_mean_NC={chromatin_test.get('delta_mean_nc', 0):+.2f}")

    if variant_results is not None:
        logging.info("")
        logging.info("--- Variant Effect (Empirical delta) ---")
        for label in bin_labels:
            if label not in variant_results:
                continue
            vr = variant_results[label]
            n_sig = int(np.sum(vr['q_values'] < 0.10))
            n_clust = len(vr.get('significant_clusters', []))
            logging.info(f"  {label}: {n_sig} pos FDR<0.10, "
                         f"{n_clust} sig clusters, "
                         f"max |delta|={np.max(np.abs(vr['delta_obs'])):.4f}")
