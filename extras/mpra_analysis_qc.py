def write_results_hdf5(output_path, parse_stats, ground_truth_nc,
                       chromatin_test, null_results_primary,
                       null_results_by_depth, variant_results,
                       filter_results, power_results,
                       bin_labels, ref_length, ref_name,
                       promoter_start, promoter_end, args):
    logging.info(f"Writing results to {output_path}...")
    with h5py.File(output_path, 'w') as f:
        meta = f.create_group('metadata')
        meta.attrs['version'] = VERSION
        meta.attrs['date'] = datetime.now().isoformat()
        meta.attrs['reference_name'] = ref_name
        meta.attrs['reference_length'] = ref_length
        meta.attrs['promoter_start'] = promoter_start
        meta.attrs['promoter_end'] = promoter_end
        meta.attrs['bin_labels'] = [l.encode() for l in bin_labels]
        if args.variant_id:
            meta.attrs['variant_id'] = args.variant_id
        if args.variant_position is not None:
            meta.attrs['variant_position'] = args.variant_position
        for k, v in parse_stats.items():
            meta.attrs[f'parse_{k}'] = v
        if null_results_primary:
            ar = null_results_primary['analysis_region']
            meta.attrs['analysis_start'] = ar[0]
            meta.attrs['analysis_end'] = ar[1]

        if ground_truth_nc is not None:
            gtn = f.create_group('ground_truth_nc')
            gtn.create_dataset('nc_vals', data=ground_truth_nc['nc_vals'])
            gtn.create_dataset('nc_fracs', data=ground_truth_nc['nc_fracs'])
            gtn.attrs['n_reads'] = ground_truth_nc['n_reads']

        if chromatin_test is not None:
            csg = f.create_group('chromatin_shift')
            for k, v in chromatin_test.items():
                if v is not None and isinstance(v, (int, float, str, bool)):
                    csg.attrs[k] = v
                elif isinstance(v, dict):
                    sg = csg.create_group(k)
                    for kk, vv in v.items():
                        sg.attrs[str(kk)] = vv

        if null_results_primary is not None:
            ng = f.create_group('null_calibration')
            ng.attrs['coverage_level'] = null_results_primary['coverage_level']
            ng.attrs['n_iterations'] = null_results_primary['n_iterations']
            ng.create_dataset('null_delta',
                              data=null_results_primary['null_delta'],
                              compression='gzip', compression_opts=4)
            for label in bin_labels:
                lg = ng.create_group(safe_hdf5_name(label))
                summ = null_results_primary['summary'][label]
                lg.create_dataset('pos_null_mean', data=summ['pos_null_mean'],
                                  compression='gzip')
                lg.create_dataset('pos_null_std', data=summ['pos_null_std'],
                                  compression='gzip')
                lg.create_dataset('wt_occ',
                                  data=null_results_primary['wt_occ'][label],
                                  compression='gzip')
                for k, v in summ['global_abs_delta_percentiles'].items():
                    lg.attrs[f'null_abs_delta_p{k}'] = v
                csums = null_results_primary['null_cluster_sums'].get(
                    label, np.array([]))
                if len(csums) > 0:
                    lg.create_dataset('null_cluster_sums', data=csums)
                cwidths = null_results_primary['null_cluster_widths'].get(
                    label, np.array([]))
                if len(cwidths) > 0:
                    lg.create_dataset('null_cluster_widths', data=cwidths)

        # Null summaries at other depths (no full delta matrix)
        if null_results_by_depth:
            ndg = f.create_group('null_by_depth')
            primary_depth = (null_results_primary['coverage_level']
                             if null_results_primary else -1)
            for depth, null_res in null_results_by_depth.items():
                if depth == primary_depth:
                    continue
                dg = ndg.create_group(str(depth))
                dg.attrs['coverage_level'] = depth
                dg.attrs['n_iterations'] = null_res['n_iterations']
                for label in bin_labels:
                    lg = dg.create_group(safe_hdf5_name(label))
                    summ = null_res['summary'][label]
                    lg.create_dataset('pos_null_mean',
                                      data=summ['pos_null_mean'],
                                      compression='gzip')
                    lg.create_dataset('pos_null_std',
                                      data=summ['pos_null_std'],
                                      compression='gzip')

        if variant_results is not None:
            vg = f.create_group('variant_results')
            for label in bin_labels:
                if label not in variant_results:
                    continue
                vr = variant_results[label]
                lg = vg.create_group(safe_hdf5_name(label))
                for key in ['delta_obs', 'variant_occ', 'wt_occ',
                            'empirical_p', 'q_values', 'z_scores']:
                    lg.create_dataset(key, data=vr[key], compression='gzip')
                lg.attrs['variant_n_raw'] = vr['variant_n_raw']
                lg.attrs['variant_n_nc_matched'] = vr['variant_n_nc_matched']
                for k, v in vr.get('comparison', {}).items():
                    lg.attrs[f'comparison_{k}'] = v

                if vr['significant_clusters']:
                    cgrp = lg.create_group('significant_clusters')
                    for i, cl in enumerate(vr['significant_clusters']):
                        sg = cgrp.create_group(f'c{i}')
                        for k, v in cl.items():
                            if isinstance(v, (int, float, str, bool)):
                                sg.attrs[k] = v

        if filter_results:
            fg = f.create_group('filter_optimization')
            for key, metrics in filter_results.items():
                sg = fg.create_group(key)
                for k, v in metrics.items():
                    if v is not None and isinstance(v, (int, float, str, bool)):
                        sg.attrs[k] = v

        if power_results:
            pg = f.create_group('power_analysis')
            for depth, depth_res in power_results.items():
                dg = pg.create_group(str(depth))
                for label, data in depth_res.items():
                    lg = dg.create_group(safe_hdf5_name(label))
                    for k, v in data.items():
                        if isinstance(v, (int, float)):
                            lg.attrs[k] = v

    logging.info(f"Results written to {output_path}")


