def power_analysis(rd, wt_idx, var_idx, ground_truth_nc,
                   null_results_by_depth, coverage_grid,
                   promoter_start, promoter_end,
                   min_nuc=None, max_nuc=None,
                   n_resamples=50, random_seed=42,
                   variant_position=None,
                   cluster_threshold_quantile=0.95,
                   gap_tolerance=2, merge_distance=5):
    """Detection rate vs coverage depth."""
    logging.info("=" * 60)
    logging.info("PHASE 6: Power Analysis")
    logging.info("=" * 60)

    bin_labels = rd.bin_labels
    var_f = rd.get_indices_filtered(var_idx, min_nuc=min_nuc, max_nuc=max_nuc)
    n_var = len(var_f)

    coverage_grid = sorted([d for d in coverage_grid if d <= n_var],
                           reverse=True)
    logging.info(f"  Variant reads: {n_var}, depths: {coverage_grid}")

    rng = np.random.default_rng(random_seed)

    first_null = list(null_results_by_depth.values())[0]
    a_start, a_end = first_null['analysis_region']
    analysis_length = a_end - a_start
    col_indices = np.arange(a_start, a_end)
    prom_rel_start = max(0, promoter_start - a_start)
    prom_rel_end = min(analysis_length, promoter_end - a_start)

    nc_vals = ground_truth_nc['nc_vals']
    nc_fracs = ground_truth_nc['nc_fracs']

    results = {}

    for depth in coverage_grid:
        available_depths = sorted(null_results_by_depth.keys())
        closest = min(available_depths, key=lambda d: abs(d - depth))
        null_res = null_results_by_depth[closest]

        detections = {l: {'any_sig_cluster': 0,
                          'any_sig_position_fdr10': 0,
                          'variant_pos_sig': 0,
                          'max_abs_delta': [],
                          'n_sig_positions': []}
                      for l in bin_labels}

        for rep in range(n_resamples):
            sub_idx = rng.choice(var_f, size=depth, replace=False)
            sub_matched, sub_n = nc_matched_subsample(
                sub_idx, rd.nuc_counts, nc_fracs, nc_vals,
                rng, replace=False)

            for bi, label in enumerate(bin_labels):
                sub_occ = rd.coverage_matrices[label][
                    np.ix_(sub_matched, col_indices)].mean(axis=0)
                delta_obs = sub_occ - null_res['wt_occ'][label]

                null_delta = null_res['null_delta'][:, bi, :]
                abs_null = np.abs(null_delta)
                abs_obs = np.abs(delta_obs)
                exceed = np.sum(abs_null >= abs_obs[np.newaxis, :], axis=0)
                emp_p = (exceed + 1) / (null_delta.shape[0] + 1)

                prom_p = emp_p[prom_rel_start:prom_rel_end]
                prom_q = benjamini_hochberg(prom_p)

                n_sig = int(np.sum(prom_q < 0.10))
                detections[label]['n_sig_positions'].append(n_sig)
                detections[label]['max_abs_delta'].append(
                    float(np.max(abs_obs)))
                if n_sig > 0:
                    detections[label]['any_sig_position_fdr10'] += 1

                # Cluster detection
                pos_thresh = np.percentile(
                    abs_null, cluster_threshold_quantile * 100, axis=0)
                sig_mask = abs_obs > pos_thresh
                clusters = _detect_clusters_from_mask(
                    sig_mask, abs_obs, delta_obs, gap_tolerance, merge_distance)

                null_csums = null_res['null_cluster_sums'].get(
                    label, np.array([]))
                has_sig = False
                for c in clusters:
                    if (c['end'] >= prom_rel_start
                            and c['start'] < prom_rel_end
                            and len(null_csums) > 0):
                        cp = float(np.mean(null_csums >= c['sum_abs_delta']))
                        if cp < 0.05:
                            has_sig = True
                            break
                if has_sig:
                    detections[label]['any_sig_cluster'] += 1

                if variant_position is not None:
                    vp_rel = variant_position - a_start
                    if 0 <= vp_rel < analysis_length:
                        q_full = np.ones(analysis_length)
                        q_full[prom_rel_start:prom_rel_end] = prom_q
                        if q_full[vp_rel] < 0.10:
                            detections[label]['variant_pos_sig'] += 1

        depth_results = {}
        for label in bin_labels:
            d = detections[label]
            depth_results[label] = {
                'detection_rate_cluster': d['any_sig_cluster'] / n_resamples,
                'detection_rate_position_fdr10':
                    d['any_sig_position_fdr10'] / n_resamples,
                'detection_rate_variant_pos':
                    d['variant_pos_sig'] / n_resamples,
                'mean_n_sig_positions': float(np.mean(d['n_sig_positions'])),
                'mean_max_abs_delta': float(np.mean(d['max_abs_delta'])),
            }
        results[depth] = depth_results

        logging.info(f"  N={depth}: " + ", ".join(
            f"{l[:3]}={depth_results[l]['detection_rate_cluster']:.0%}"
            for l in bin_labels))

    return results


