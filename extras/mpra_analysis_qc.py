                color = BIN_COLORS.get(label, 'gray')

                ax = axes[i, 0]
                ax.plot(x, vr['wt_occ'], linewidth=1.5,
                        color='#4a86c8', alpha=0.8, label='WT')
                ax.plot(x, vr['variant_occ'], linewidth=1.5,
                        color='#e85d50', alpha=0.8, label='Variant')
                ax.set_ylabel('Occupancy')
                ax.set_title(f'{BIN_SHORT.get(label, label)}: Occupancy')
                ax.legend(fontsize=8, frameon=False)
                if vp_rel is not None and vp_rel < len(x):
                    ax.axvline(x[vp_rel], color='red', alpha=0.3,
                               linestyle='--')

                ax = axes[i, 1]
                delta = vr['delta_obs']
                ax.fill_between(x, delta, alpha=0.3, color=color)
                ax.plot(x, delta, linewidth=0.8, color=color)
                ax.axhline(0, color='gray', linewidth=0.5)
                for cl in vr.get('significant_clusters', []):
                    cs, ce = cl['start'], cl['end']
