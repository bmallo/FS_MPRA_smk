def variant_effect_test(rd, wt_idx, var_idx, ground_truth_nc, null_results,
                        promoter_start, promoter_end,
                        min_nuc=None, max_nuc=None, random_seed=42,
                        cluster_threshold_quantile=0.95,
                        absolute_delta_threshold=None,
                        gap_tolerance=2, merge_distance=5):
    """Test variant effect using empirical delta approach.

    Primary: position-level and cluster-level empirical p-values.
    Secondary: lightweight Z-scores from null delta mean/std.
    """
    logging.info("=" * 60)
    logging.info("PHASE 4: Variant Effect Testing")
    logging.info("=" * 60)

    bin_labels = rd.bin_labels
    a_start, a_end = null_results['analysis_region']
    analysis_length = null_results['analysis_length']

    prom_rel_start = max(0, promoter_start - a_start)
    prom_rel_end = min(analysis_length, promoter_end - a_start)
    prom_slice = slice(prom_rel_start, prom_rel_end)

    var_f = rd.get_indices_filtered(var_idx, min_nuc=min_nuc, max_nuc=max_nuc)
    nc_vals = ground_truth_nc['nc_vals']
    nc_fracs = ground_truth_nc['nc_fracs']
    rng = np.random.default_rng(random_seed)

    var_matched, var_n = nc_matched_subsample(
        var_f, rd.nuc_counts, nc_fracs, nc_vals, rng, replace=False)
    logging.info(f"  Variant: {len(var_f)} filtered, {var_n} NC-matched")

    col_indices = np.arange(a_start, a_end)
    wt_occ = null_results['wt_occ']

    results = {}
    for bi, label in enumerate(bin_labels):
        var_occ = rd.coverage_matrices[label][
            np.ix_(var_matched, col_indices)].mean(axis=0)

        delta_obs = var_occ - wt_occ[label]

        # Position-level empirical p-values
        null_delta = null_results['null_delta'][:, bi, :]
        n_null = null_delta.shape[0]
        abs_delta_obs = np.abs(delta_obs)
        abs_null = np.abs(null_delta)

        # Vectorized: count null iterations with |delta_null| >= |delta_obs|
        exceed_count = np.sum(
            abs_null >= abs_delta_obs[np.newaxis, :], axis=0)
        empirical_p = (exceed_count + 1) / (n_null + 1)

        # BH-FDR within promoter
        prom_p = empirical_p[prom_slice]
        prom_q = benjamini_hochberg(prom_p)
        q_values = np.ones(analysis_length)
        q_values[prom_slice] = prom_q

        # Lightweight Z-scores from null delta distribution
        null_mean = null_results['summary'][label]['pos_null_mean']
        null_std = null_results['summary'][label]['pos_null_std']
        null_std_floored = np.maximum(null_std, 1e-6)
        z_scores = (delta_obs - null_mean) / null_std_floored
        z_scores = np.nan_to_num(z_scores, nan=0.0)

        # Cluster detection: per-position null threshold
        pos_thresh = np.percentile(abs_null,
                                   cluster_threshold_quantile * 100, axis=0)
        sig_mask = abs_delta_obs > pos_thresh
        if absolute_delta_threshold is not None:
            sig_mask &= abs_delta_obs > absolute_delta_threshold

        clusters = _detect_clusters_from_mask(
            sig_mask, abs_delta_obs, delta_obs,
            gap_tolerance, merge_distance, min_width=3)

        # Cluster p-values
        null_csums = null_results['null_cluster_sums'].get(label, np.array([]))
        null_max_csums = null_results['null_max_cluster_sums'].get(
            label, np.array([]))

        for c in clusters:
            if len(null_csums) > 0:
                c['sum_p'] = max(
                    float(np.mean(null_csums >= c['sum_abs_delta'])),
                    1.0 / (len(null_csums) + 1))
            else:
                c['sum_p'] = np.nan
            if len(null_max_csums) > 0:
                c['max_sum_p'] = float(
                    np.mean(null_max_csums >= c['sum_abs_delta']))
            else:
                c['max_sum_p'] = np.nan

            expected = CLUSTER_WIDTH_RANGES.get(label, (3, 200))
            c['biologically_plausible'] = (
                expected[0] <= c['width'] <= expected[1])
            c['abs_start'] = c['start'] + a_start
            c['abs_end'] = c['end'] + a_start

        prom_clusters = [c for c in clusters
                         if c['end'] >= prom_rel_start
                         and c['start'] < prom_rel_end]
        sig_clusters = [c for c in prom_clusters
                        if c.get('sum_p', 1.0) < 0.05]

        # Compare empirical vs Z-score calls
        z_sig = np.abs(z_scores) > 2.0
        emp_sig = empirical_p < 0.05
        comparison = {
            'agree': int(np.sum(z_sig == emp_sig)),
            'disagree': int(np.sum(z_sig != emp_sig)),
            'only_z': int(np.sum(z_sig & ~emp_sig)),
            'only_empirical': int(np.sum(~z_sig & emp_sig)),
        }

        res = {
            'delta_obs': delta_obs, 'variant_occ': var_occ,
            'wt_occ': wt_occ[label],
            'empirical_p': empirical_p, 'q_values': q_values,
            'z_scores': z_scores, 'null_mean': null_mean, 'null_std': null_std,
            'variant_n_raw': len(var_f), 'variant_n_nc_matched': var_n,
            'all_clusters': prom_clusters,
            'significant_clusters': sig_clusters,
            'analysis_region': (a_start, a_end),
            'comparison': comparison,
        }
        results[label] = res

        n_sig_pos = int(np.sum(q_values[prom_slice] < 0.10))
        logging.info(f"  {label}: {n_sig_pos} positions FDR<0.10, "
                     f"{len(sig_clusters)} sig clusters")
        for c in sig_clusters:
            logging.info(f"    [{c['abs_start']}-{c['abs_end']}] "
                         f"w={c['width']}bp "
                         f"sum|delta|={c['sum_abs_delta']:.3f} "
                         f"p={c['sum_p']:.4f} dir={c['direction']}")

    return results


