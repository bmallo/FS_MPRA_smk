def _null_worker_shared(args):
    """Worker for null iterations using shared memory matrices."""
    (it_seeds, shm_info, bin_labels, nc_draw_counts,
     wt_nc_pools_serialized, subsample_size, wt_occ_list,
     analysis_length, cluster_threshold_quantile, gap_tolerance,
     merge_distance, absolute_delta_threshold) = args

    n_iters = len(it_seeds)
    n_bins = len(bin_labels)

    # Attach to shared memory
    shared_mats = {}
    shm_handles = []
    for label in bin_labels:
        arr, shm = load_shared_matrix(shm_info[label])
        shared_mats[label] = arr
        shm_handles.append(shm)

    wt_nc_pools = {nc_val: pool for nc_val, pool in wt_nc_pools_serialized}
    wt_occ = {label: np.array(vals) for label, vals in zip(bin_labels, wt_occ_list)}

    delta_out = np.zeros((n_iters, n_bins, analysis_length), dtype=np.float32)
    cluster_stats = []

    for local_i, seed in enumerate(it_seeds):
        rng = np.random.default_rng(seed)
        pv_indices = _nc_matched_batch_draw(
            nc_draw_counts, wt_nc_pools, 1, subsample_size, rng)[0]

        iter_clusters = {}
        for bi, label in enumerate(bin_labels):
            pv_occ = shared_mats[label][pv_indices].mean(axis=0)
            delta = pv_occ - wt_occ[label]
            delta_out[local_i, bi, :] = delta.astype(np.float32)

            abs_delta = np.abs(delta)
            thresh = 0.0
            if cluster_threshold_quantile is not None:
                thresh = np.percentile(abs_delta,
                                       cluster_threshold_quantile * 100)
            if absolute_delta_threshold is not None:
                thresh = max(thresh, absolute_delta_threshold)

            clusters = _detect_clusters_adaptive(
                abs_delta, thresh, gap_tolerance, merge_distance)
            iter_clusters[label] = {
                'n_clusters': len(clusters),
                'max_cluster_sum': (max(c['sum_abs_delta'] for c in clusters)
                                    if clusters else 0.0),
                'cluster_sums': [c['sum_abs_delta'] for c in clusters],
                'cluster_widths': [c['width'] for c in clusters],
            }
        cluster_stats.append(iter_clusters)

    for shm in shm_handles:
        shm.close()

    return delta_out, cluster_stats


