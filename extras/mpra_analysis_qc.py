def filter_optimization(rd, wt_idx, var_idx, bin_labels,
                        min_nuc_grid, max_nuc_percentiles,
                        promoter_start, promoter_end,
                        variant_position=None,
                        n_null_iterations=100,
                        random_seed=42, analysis_region=None,
                        cluster_threshold_quantile=0.95,
                        gap_tolerance=2, merge_distance=5,
                        n_workers=1):
    """Test detection across a grid of NC filters."""
    logging.info("=" * 60)
    logging.info("PHASE 5: Filter Optimization")
    logging.info("=" * 60)

    all_nc = rd.nuc_counts[rd.nuc_counts >= 0]
    max_nuc_values = {}
    for pctile in max_nuc_percentiles:
        if pctile >= 100:
            max_nuc_values[pctile] = None
        else:
            max_nuc_values[pctile] = int(np.percentile(all_nc, pctile))
    logging.info(f"  Max NC from percentiles: {max_nuc_values}")

    grid_results = {}

    for min_nc in min_nuc_grid:
        for max_pctile in max_nuc_percentiles:
            max_nc = max_nuc_values[max_pctile]
            label_key = f"min{min_nc}_maxP{max_pctile}"

            wt_f = rd.get_indices_filtered(wt_idx, min_nuc=min_nc,
                                           max_nuc=max_nc)
            var_f = rd.get_indices_filtered(var_idx, min_nuc=min_nc,
                                            max_nuc=max_nc)

            if len(wt_f) < 50 or len(var_f) < 20:
                logging.info(f"  {label_key}: skipped "
                             f"(WT={len(wt_f)}, var={len(var_f)})")
                continue

            gt_nc = compute_ground_truth_nc(rd, wt_idx, min_nuc=min_nc,
                                            max_nuc=max_nc)
            if gt_nc is None:
                continue

            null_res = run_null_calibration(
                rd, wt_idx, gt_nc, coverage_level=len(var_f),
                min_nuc=min_nc, max_nuc=max_nc,
                n_iterations=n_null_iterations,
                random_seed=random_seed,
                analysis_region=analysis_region,
                cluster_threshold_quantile=cluster_threshold_quantile,
                gap_tolerance=gap_tolerance, merge_distance=merge_distance,
                n_workers=max(1, n_workers // 4))

            var_res = variant_effect_test(
                rd, wt_idx, var_idx, gt_nc, null_res,
                promoter_start, promoter_end,
                min_nuc=min_nc, max_nuc=max_nc,
                random_seed=random_seed,
                cluster_threshold_quantile=cluster_threshold_quantile,
                gap_tolerance=gap_tolerance, merge_distance=merge_distance)

            metrics = {'wt_n': len(wt_f), 'var_n': len(var_f),
                       'min_nuc': min_nc, 'max_nuc_pctile': max_pctile,
                       'max_nuc_value': max_nc if max_nc is not None else -1}

            a_start_r = null_res['analysis_region'][0]

            for bl in bin_labels:
                if bl not in var_res:
                    continue
                vr = var_res[bl]
                prom_q = vr['q_values']
                metrics[f'{bl}_n_sig_fdr10'] = int(np.sum(prom_q < 0.10))
                metrics[f'{bl}_n_sig_clusters'] = len(
                    vr['significant_clusters'])
                metrics[f'{bl}_max_abs_delta'] = float(
                    np.max(np.abs(vr['delta_obs'])))

                null_med = np.median(
                    np.abs(null_res['null_delta'][
                        :, bin_labels.index(bl), :]).flatten())
                if null_med > 0:
                    metrics[f'{bl}_snr'] = float(
                        np.max(np.abs(vr['delta_obs'])) / null_med)
                else:
                    metrics[f'{bl}_snr'] = float('nan')

                if variant_position is not None:
                    vp_rel = variant_position - a_start_r
                    if 0 <= vp_rel < null_res['analysis_length']:
                        metrics[f'{bl}_delta_at_variant'] = float(
                            vr['delta_obs'][vp_rel])

            grid_results[label_key] = metrics
            logging.info(f"  {label_key}: WT={len(wt_f):,}, var={len(var_f):,}")

    return grid_results


