    with PdfPages(pdf_path) as pdf:
        # ---- Title page (ensures PDF is never empty) ----
        fig, ax = plt.subplots(figsize=(14, 3))
        ax.axis('off')
        ax.text(0.5, 0.7, f'MPRA Empirical Analysis v{VERSION}',
                fontsize=18, ha='center', va='center', fontweight='bold')
        info_lines = []
        if chromatin_test is not None:
            info_lines.append(f"WT reads: {chromatin_test.get('wt_n', '?'):,}")
            info_lines.append(f"Variant reads: {chromatin_test.get('var_n', '?'):,}")
        if variant_results is not None:
            info_lines.append("Variant results: YES")
        else:
            info_lines.append("Variant results: NONE (check logs for filtering issues)")
        ax.text(0.5, 0.3, '\n'.join(info_lines),
                fontsize=11, ha='center', va='center', color='gray')
        pdf.savefig(fig, bbox_inches='tight')
        plt.close(fig)
        # ---- Page 1: NC distributions ----
        if chromatin_test and 'wt_nc_distribution' in chromatin_test:
            fig, axes = plt.subplots(1, 2, figsize=(14, 5))
            wt_dist = chromatin_test['wt_nc_distribution']
            var_dist = chromatin_test['var_nc_distribution']
            all_ncs = sorted(set(list(wt_dist.keys()) + list(var_dist.keys())))

            ax = axes[0]
            if all_ncs:
                wt_c = [wt_dist.get(nc, 0) for nc in all_ncs]
                var_c = [var_dist.get(nc, 0) for nc in all_ncs]
                wt_tot = max(sum(wt_c), 1)
                var_tot = max(sum(var_c), 1)
                w = 0.35
                pos = np.arange(len(all_ncs))
                ax.bar(pos - w/2, [c/wt_tot for c in wt_c], w,
                       label='WT', color='#4a86c8', alpha=0.7)
                ax.bar(pos + w/2, [c/var_tot for c in var_c], w,
                       label='Variant', color='#e85d50', alpha=0.7)
                ax.set_xticks(pos)
                ax.set_xticklabels(all_ncs)
                ax.set_xlabel('Nucleosome Count')
                ax.set_ylabel('Fraction')
                ax.legend()
                ks_p = chromatin_test.get('ks_pval', float('nan'))
                ax.set_title(f'NC Distribution (KS p={ks_p:.2e})')

            ax = axes[1]
            if all_ncs:
                wt_cdf = np.cumsum([c/wt_tot for c in wt_c])
                var_cdf = np.cumsum([c/var_tot for c in var_c])
                ax.step(all_ncs, wt_cdf, where='mid',
                        label='WT', color='#4a86c8', linewidth=2)
                ax.step(all_ncs, var_cdf, where='mid',
                        label='Variant', color='#e85d50', linewidth=2)
                ax.set_xlabel('Nucleosome Count')
                ax.set_ylabel('Cumulative Fraction')
                ax.legend()
                ax.set_title('NC CDF')

            fig.suptitle('Phase 2: Chromatin Shift Test', fontsize=14)
            plt.tight_layout()
            pdf.savefig(fig, bbox_inches='tight')
            plt.close(fig)

        # ---- Page 2: Null calibration QQ + null variability ----
        if null_results_primary is not None:
            fig, axes = plt.subplots(len(bin_labels), 2,
                                     figsize=(14, 3.5 * len(bin_labels)))
            if len(bin_labels) == 1:
                axes = axes.reshape(1, -1)

            for i, label in enumerate(bin_labels):
                summ = null_results_primary['summary'][label]

                ax = axes[i, 0]
                null_flat = null_results_primary['null_delta'][
                    :, i, :].flatten()
                n_qq = min(10000, len(null_flat))
                rng_qq = np.random.default_rng(42)
                qq_sample = np.sort(rng_qq.choice(null_flat, size=n_qq,
                                                   replace=False))
                theoretical = np.sort(rng_qq.normal(0, np.std(null_flat),
                                                     size=n_qq))
                ax.scatter(theoretical, qq_sample, s=1, alpha=0.3,
                           color=BIN_COLORS.get(label, 'gray'))
                lim = max(abs(qq_sample).max(), abs(theoretical).max()) * 1.1
                ax.plot([-lim, lim], [-lim, lim], 'k--', linewidth=0.5)
                ax.set_xlabel('Theoretical (Normal)')
                ax.set_ylabel('Observed Null Δ')
                ax.set_title(f'{BIN_SHORT.get(label, label)}: QQ Plot')

                ax = axes[i, 1]
                ax.fill_between(x, summ['pos_null_std'], alpha=0.4,
                                color=BIN_COLORS.get(label, 'gray'))
                ax.plot(x, summ['pos_null_std'], linewidth=0.8,
                        color=BIN_COLORS.get(label, 'gray'))
                ax.set_ylabel('Null Δ std')
                ax.set_title(f'{BIN_SHORT.get(label, label)}: '
                             f'Per-Position Null Variability')
                if vp_rel is not None and vp_rel < len(x):
                    ax.axvline(x[vp_rel], color='red', alpha=0.5,
                               linestyle='--', linewidth=1)

            fig.suptitle('Phase 3: Null Calibration Diagnostics', fontsize=14)
            plt.tight_layout()
            pdf.savefig(fig, bbox_inches='tight')
            plt.close(fig)

        # ---- Page 3: Occupancy and delta ----
        if variant_results is not None:
            fig, axes = plt.subplots(len(bin_labels), 2,
                                     figsize=(14, 3.5 * len(bin_labels)))
            if len(bin_labels) == 1:
                axes = axes.reshape(1, -1)

            for i, label in enumerate(bin_labels):
                if label not in variant_results:
                    continue
